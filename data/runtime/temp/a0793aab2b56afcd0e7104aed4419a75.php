    function openTopframe(obj,title,width,height,offset){
        //iframe窗
        var url = '';
        if (typeof(obj) == 'string' && obj.indexOf("?m=admin&c=") != -1) {
            url = obj;
        } else {
            url = $(obj).data('href');
        }
        if (!width) width = '100%';
        if (!height) height = '100%';
        if (!offset) offset = 'auto';

        var anim = 0;
        var shade = layer_shade;
        if ('r' == offset) {
            shade = [0.05];
            anim = 5;
        }
        var iframes = top.layer.open({
            type: 2,
            title: title,
            fixed: true, //不固定
            shadeClose: false,
            shade: shade,
            offset: offset,
            closeBtn: 3,
            // maxmin: true, //开启最大化最小化按钮
            area: [width, height],
            anim: anim,
            content: url,
            end: function() {
                if (1 == $(obj).data('closereload')) window.location.reload();
            },
            success: function(layero, index){
                if ('r' == offset) {
                    $('.layui-layer-shade').click(function(){
                        layer.close(index);
                    });
                }
            }
        });
        if (width == '100%' && height == '100%') {
            layer.full(iframes);
        }
    }
